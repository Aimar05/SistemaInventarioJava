    private void buscar(){
        Integer id = null;
        String usuario = txtUsuario.getText().trim();
        
        if (!txtID.getText().trim().isEmpty()) {
            try {
                id = Integer.parseInt(txtID.getText().trim());
            } catch (NumberFormatException e) {
                return;
            }
        }
        List<Usuario> lista = usud.buscarUsuario(id, usuario);
        listar(lista);
    }

